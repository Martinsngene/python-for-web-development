from math import *

# Working with numbers, methods and numerical operations

# Number To String
num = 5
print(str(num) + "2")

# Getting the absolute value of numbers
negative_number = -5
print(abs(negative_number))

# Raising numbers to power
print(pow(5, 2))

# Return Larger Number
print(max(10, 24))

# Return Smaller Number
print(min(10, 24))

# Rounds up or rounds off numbers as the case may be
print(round(10.2))

# Round off a number
print(floor(3.6))

# Round up a number
print(ceil(3.6))

# Return the square root of a number
print(sqrt(49))

