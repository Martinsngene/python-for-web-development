# Creating a tuple
coordinates = (4,5)
# tuples are like lists(arrays), but they are immutable
print(coordinates[1])

# Tuples are data structures for storing data that should be immutable. We can also create a list of tuples
map_coordinates = [(23, 45), (2, 7), (9, 0), (67, 23)]

# Accessing the data in a list of tuples
print(map_coordinates[0][0])